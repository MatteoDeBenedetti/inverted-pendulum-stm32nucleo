/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  SysTick_Config(SystemCoreClock/100);  // set systick interrupt
  velpos_controller_initialize(); // include matlab function
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM3_Init();
  MX_USART3_UART_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL); // start timer in encoder mode
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  HAL_GPIO_WritePin(GPIOF, GPIO_PIN_13, 1); // Eanble H-BRIDGE M1
  dutycycle = 0;// set duty cycle value=0-4095
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1); // start pwm on motor1
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, dutycycle); // update duty cycle

  HAL_Delay(3000);

  while (1)
  {
		
		short int cnt = (short int)htim3.Instance->CNT; // read encoder pulses
		deg = (cnt*360)/2000; // convert to deg


		// set variables for the matlab step fcn
		theta_err = theta_ref - deg;
		velpos_controller_U.In1 = theta_ref;
		velpos_controller_U.In2 = deg;
		
		dutycycle = V_in/24*4095; // set duty cycle
		
		// set direction
		if (dutycycle >= 0)
		{
		  HAL_GPIO_WritePin(GPIOG, GPIO_PIN_2, 1); // INA
		  HAL_GPIO_WritePin(GPIOG, GPIO_PIN_3, 0); // INB
		}
		else
		{
		  HAL_GPIO_WritePin(GPIOG, GPIO_PIN_2, 0); // INA
		  HAL_GPIO_WritePin(GPIOG, GPIO_PIN_3, 1); // INB
		}
		
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, fabs(dutycycle)); // update duty cycle


  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */

}

